# Q01: While Basic
# 1から3まで数値を表示する
# ここにコードを書いてください
counter = 1
while counter <= 3:
    print(counter)
    counter += 1


# Q02: While Counter
# 0から4まで数値を表示する
# ここにコードを書いてください
counter = 0
while counter <= 4:
    print(counter)
    counter += 1


# Q03: While Condition
# "Hello"を3回表示する
# ここにコードを書いてください
counter = 0
while counter < 3:
    print("Hello")
    counter += 1
